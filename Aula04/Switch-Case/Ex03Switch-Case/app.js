// // 3. **Exercício Intermediário:**
// // Escreva um programa em JavaScript que recebe uma letra do alfabeto e retorna se é uma vogal ou uma consoante.
// Utilize a estrutura `switch/case` para realizar essa verificação.

let letra = "s";

switch (letra) {
  case "a":
    console.log("Vogal");
    break;
  case "e":
    console.log("Vogal");
    break;
  case "i":
    console.log("Vogal");
    break;
  case "o":
    console.log("Vogal");
    break;
  case "u":
    console.log("Vogal");
    break;

  default:
    console.log("Consoante");
    break;
}
