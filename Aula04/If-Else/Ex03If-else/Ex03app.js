// Escreva um programa em JavaScript que verifica se um número é divisível por 3 e por 5 ao mesmo tempo. 
// Use a estrutura `if-else` para realizar essa verificação.

let numero = 15;
if (numero % 5 == 0 && numero % 3 == 0) {
    console.log('Número é divisivel por 5 e 3 !!!');
} else {
    console.log('Número nao é divisivel por 5 e 3 !!!');
} 